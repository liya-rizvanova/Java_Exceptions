/*Напишите программу для выполнения арифметической операции деления двух целых чисел a и b.
При этом программа должна проверить, что делитель b не равен нулю, и выполнить деление только в этом случае.
Если b равен нулю, программа должна вернуть результат равный нулю.
После выполнения операции деления, программа также должна вывести сумму чисел a и b с помощью метода printSum.
Если аргументы не переданы через командную строку, используйте значения по умолчанию.

На входе:
'12'
'5'

На выходе:
17
2.4 */
package ru.rizvanova.exc3;

class Expr {

    public static double expr(int a, int b) {
        // Проверяем, что делитель b не равен нулю
        if (b != 0) {
            // Проверяем, что a и b - целые числа
            if (a % b == 0) {
                // Если оба числа целые, возвращаем целый результат деления
                return a / b;
            } else {
                // Иначе возвращаем результат деления в виде вещественного числа
                return (double) a / b;
            }
        } else {
            // Если делитель равен нулю, возвращаем ноль
            return 0;
        }
    }

    public static void printSum(int a, int b) {
        // Выводим сумму чисел a и b
        System.out.println(a + b);
    }
}

// Не удаляйте этот класс - он нужен для вывода результатов на экран и проверки
public class Printer {
    public static void main(String[] args) {
        int a;
        int b;

        if (args.length == 0) {
            a = 12;
            b = 5; // Значения по умолчанию, если аргументы не переданы через командную строку
        } else {
            a = Integer.parseInt(args[0]);
            b = Integer.parseInt(args[1]);
        }
        
        // Выводим сумму чисел a и b
        Expr.printSum(a, b);
      
        // Выполняем арифметическую операцию деления и выводим результат
        double result = Expr.expr(a, b);
        System.out.println(result);

    }
}
